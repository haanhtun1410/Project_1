    private void fillTable() {
        bang.setRowCount(0);
        for(Nsx x: SV.getNSX()){
            bang.addRow(new Object[]{x.getId(),x.getTen()});
        }
    }

    private void clearForm() {
        txtID.setText("");
        txtTen.setText("");
    }

    private Nsx getData() {
        Nsx a = new Nsx();
        if(txtID.getText().trim().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Nhập ID");
            return null;
        }else if(txtTen.getText().trim().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Nhập tên");
            return null;
        }else{
            a.setId(txtID.getText());
            a.setTen(txtTen.getText());
            return a;
        }
    }
