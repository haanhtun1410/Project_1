    private void btnDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoiActionPerformed
        List<User> u = NVS.getAll1();
        boolean k = false;
        
        for(User x: u){
            if(x.getId().equals(txtUser.getText().trim())){
                k = true;
                break;
            }
        }
        
        if(k==false){
            JOptionPane.showMessageDialog(this, "Người dùng này không tồn tại");
        }
        else{
            User l = NVS.getById(txtUser.getText().trim());
            if(l.getMatKhau().equals(pssMatKhau.getText()))
            {
                if(pssMatKhauMoi.getText().trim().isEmpty())
                {
                    JOptionPane.showMessageDialog(this, "Yêu cầu điền mật khẩu mới");
                }
                else{
                    if(pssNhapLai.getText().equals(pssMatKhauMoi.getText())){
                        l.setMatKhau(pssMatKhauMoi.getText().trim());
                        NVS.update(l);
                        JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công");
                        clearForm();
                    }else{
                        JOptionPane.showMessageDialog(this, "Nhập lại đúng mật khẩu mới");
                    }
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Nhập đúng mật khẩu");
            }
        }
    }//GEN-LAST:event_btnDoiActionPerformed

